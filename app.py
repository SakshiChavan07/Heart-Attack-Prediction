# app.py

import streamlit as st
import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression

# -----------------------
# Model Training (instead of loading from pkl)
# -----------------------

# Example: Training Data (or you can load CSV)
# Create dummy data
X = np.random.rand(200, 3)  # 3 features: age, heart_rate, blood_sugar
y = np.random.randint(0, 2, 200)  # 0 or 1

# Split data
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)

# Train model
model = LogisticRegression()
model.fit(X_train, y_train)

# -----------------------
# Streamlit App
# -----------------------

st.title('Heart Attack Risk Prediction')

# User Inputs
age = st.number_input('Age', min_value=1, max_value=120)
heart_rate = st.number_input('Heart Rate', min_value=30, max_value=220)
blood_sugar = st.number_input('Blood Sugar', min_value=50, max_value=300)

# Predict button
if st.button('Predict'):
    pred = model.predict([[age, heart_rate, blood_sugar]])
    if pred[0] == 1:
        st.error('⚠️ High Risk of Heart Attack!')
    else:
        st.success('✅ Low Risk of Heart Attack.')
